package ex_06_Ternary_Operator;

public class Lab061_TO {
    public static void main(String[] args) {
        // Find the maximum number between two numbers.
        int X = 10;
        int Y =20;
        System.out.println(Math.min(X,Y));

        int max = X>Y ? 10:20;
        System.out.println(max);
    }
}
